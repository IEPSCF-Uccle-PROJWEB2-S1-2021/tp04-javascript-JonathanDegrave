//recherche minimum
function minimum(a,b){
    if(a === b || a < b)
    {
        return a;
    }
    else
    {
        return b;
    }
}


//recherche maximum
function maximum(a,b){
    if(a === b || a > b)
    {
        return a;
    }
    else
    {
        return b;
    }
}


